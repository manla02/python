# variable names are case sensitive
# A variable cannot be a reserved key words ie words already used by python e.g "print"
# as best practices; when creating variables do not use spaces
# you can either use underscore or carmel case

# Using underscores or Snake case
user_accounts = ""
user_data_transactions = ""

# Using carmel case
userAccounts = ""
